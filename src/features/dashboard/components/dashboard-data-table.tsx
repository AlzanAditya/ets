/**
 * Purpose: adapt the reusable DataTable to the dashboard section workflow.
 * Responsibilities: provide dashboard columns, labels, tabs, and row fallback data.
 * Expected props: dashboard table rows from page data or API responses.
 * Usage notes: placeholder rows are generic and only used for development preview.
 */
export function DashboardDataTable({
  data = placeholderRows,
}: {
  data?: DashboardTableRow[]
}) {
  return (
    <DataTable
      persistenceKey="dashboard-feature"
      addButtonLabel="Add Section"
      columns={dashboardColumns}
      data={data}
      defaultTab="outline"
      tabs={dashboardTabs}
    />
  )
}
